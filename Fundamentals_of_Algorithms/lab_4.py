"""13. Написать программу, которая позволяет посчитать цену жалюзи.
 Исходные данные: размер (ширина и высота, выраженные в сантиметрах) 
 и тип материала (пластик, текстиль, алюминий). Если сумма покупки больше 1000 сомов,
  то предоставляется скидка 5%.
"""

width = float(input("Введите ширину (см): "))
height = float(input("Введите высоту (см): "))
material = input("Введите материал (пластик/текстиль/алюминий): ").lower()
area = width * height

match material:
    case "пластик":
        price_per_cm2 = 0.5
    case "текстиль":
        price_per_cm2 = 0.7
    case "алюминий":
        price_per_cm2 = 1.0
    case _:
        print("Ошибка: неизвестный материал!")
        exit()

total = area * price_per_cm2
if total > 1000:
    total *= 0.95

print(f"\nРазмер: {width} x {height} см")
print(f"Материал: {material.capitalize()}")
print(f"Цена: {total:.2f} сом")

